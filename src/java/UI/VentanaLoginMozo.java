    private void ingresarMozo(String nombre, String clave) {
        if(nombre.isEmpty()){
           JOptionPane.showMessageDialog(this, "Debe ingresar un nombre!");
        }else if(clave.isEmpty()){
            JOptionPane.showMessageDialog(this, "Debe ingresar su clave!");
        }else{
            controlador.LoginMozo(nombre, clave);
        }
    }

